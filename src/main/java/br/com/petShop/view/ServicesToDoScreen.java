    private void editBntActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBntActionPerformed
        // TODO add your handling code here

        int selectedRow = servicesTable.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "É necessário selecionar um item da tabela para conseguir editar.");
            return;
        }

        Customers f = new Customers();
        CustomersDAO dao = new CustomersDAO();

        Pets p = new Pets();
        PetsDAO petsDAO = new PetsDAO();

        Services s = new Services();
        ServicesDAO servicesDAO = new ServicesDAO();

        ToDoServicesScreen scheduleservices = new ToDoServicesScreen();

        scheduleservices.idField.setText(servicesTable.getValueAt(selectedRow, 0).toString());

        f = dao.consultCustomers(servicesTable.getValueAt(selectedRow, 1).toString());
        scheduleservices.CustomersField.removeAllItems();
        scheduleservices.CustomersField.getModel().setSelectedItem(f);

        p = petsDAO.getPetByName(servicesTable.getValueAt(selectedRow, 2).toString());
        scheduleservices.petField.removeAllItems();
        scheduleservices.petField.getModel().setSelectedItem(p);

        s = servicesDAO.getServicesByName(servicesTable.getValueAt(selectedRow, 3).toString());
        scheduleservices.cbService.removeAllItems();
        scheduleservices.cbService.getModel().setSelectedItem(s);

        scheduleservices.dateField.setText(servicesTable.getValueAt(selectedRow, 4).toString());
        scheduleservices.timeField.setText(servicesTable.getValueAt(selectedRow, 5).toString());

        String status = servicesTable.getValueAt(selectedRow, 6).toString();
        scheduleservices.statusField.setSelectedItem(status);

        scheduleservices.totalField.setText(servicesTable.getValueAt(selectedRow, 7).toString());

        String totalValue = scheduleservices.totalField.getText();

        for (int i = 0; i < scheduleservices.cbPrice.getItemCount(); i++) {
            String item = scheduleservices.cbPrice.getItemAt(i).toString();
            String[] parts = item.split("R\\$ ");
            if (parts.length > 1) {
                String price = parts[1];
                if (price.equals(totalValue)) {
                    scheduleservices.cbPrice.setSelectedIndex(i);
                    break;
                }
            }
        }

        scheduleservices.setVisible(true);

    }//GEN-LAST:event_editBntActionPerformed

    private void deleteBntActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBntActionPerformed
        // TODO add your handling code here:

        int selectedRow = servicesTable.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "É necessário selecionar um item da tabela para conseguir deletar.");
            return;
        }

        int scheduleId = Integer.parseInt(servicesTable.getValueAt(selectedRow, 0).toString());

        ToDo obj = new ToDo();
        obj.setId(scheduleId);

        ServicesDAO dao = new ServicesDAO();
        dao.deleteToDo(obj);

    }//GEN-LAST:event_deleteBntActionPerformed

    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed


        try {
            DateTimeFormatter format = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDate dateStart = LocalDate.parse(dateStartField.getText(), format);
            LocalDate dateEnd;

            String dateEndText = dateEndField.getText().trim();
            if (dateEndText.isEmpty() || dateEndText.equals("//")) {
                dateEnd = dateStart;
            } else {
                dateEnd = LocalDate.parse(dateEndText, format);
            }

            ServicesDAO services_dao = new ServicesDAO();
            List<ToDo> list = services_dao.listToDo();

            DefaultTableModel data = (DefaultTableModel) servicesTable.getModel();
            data.setNumRows(0);

            for (ToDo s : list) {
                data.addRow(new Object[]{
                        s.getId(),
                        s.getDate(),
                        s.getCustumers().getName(),
                        s.getPets().getPet_name(),
                        s.getServices().getService_name(),
                        s.getTime(),
                        s.getStatus(),
                        s.getTotal_Value()
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Nenhum registro encontrado");
        }

    }//GEN-LAST:event_searchBtnActionPerformed

